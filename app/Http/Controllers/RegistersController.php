    {
        $this->validate($request,[
            'firstname'=>'required',
            'lastname'=>'required',
            'password'=>'confirmed|min:6',
            'dob'=>['required','date',new DateValidate],
            'email' => 'email',
            'contact'=>'regex:/[0-9]{2}\d{8}/'
        ]);
        return $request;
    }

